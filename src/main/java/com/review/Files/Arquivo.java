package com.review.Files;

public class Arquivo {
    public String nomeCodigoArquivo;
    public String nomeSecretoArquivo;
    public String donoArquivo;
    public String grupoArquivo;

    Arquivo(String nomeCodigoArquivo, String nomeSecretoArquivo, String donoArquivo, String grupoArquivo) {
        this.nomeCodigoArquivo = nomeCodigoArquivo;
        this.nomeSecretoArquivo = nomeSecretoArquivo;
        this.donoArquivo = donoArquivo;
        this.grupoArquivo = grupoArquivo;
    }
}
